const gba = @import("gba.zig");

const Letter = [8]u8;

pub fn letterToTile(letter: Letter) gba.Tile {
    var ret: gba.Tile = undefined;
    for (letter, 0..) |l, i| {
        var accum: u32 = 0x00000000;
        const lr: u32 = @intCast(@bitReverse(l));
        accum |= (lr & 1);
        accum |= (lr & 2) << 3;
        accum |= (lr & 4) << 6;
        accum |= (lr & 8) << 9;
        accum |= (lr & 16) << 12;
        accum |= (lr & 32) << 15;
        accum |= (lr & 64) << 18;
        accum |= (lr & 128) << 21;
        ret[i] = accum;
    }
    return ret;
}

